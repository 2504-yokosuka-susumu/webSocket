package com.example.RealTimeChat.chat;

public class User {
	// フィールド
	private String id;
	private String name;

	public String GetId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String GetName() {
		return name;
	}
}
